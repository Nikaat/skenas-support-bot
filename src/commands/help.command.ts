import { Context } from "telegraf";

export const helpCommand = {
  command: "help",
  description: "Show available commands",
  handler: async (ctx: Context): Promise<void> => {
    try {
      const helpText = `
🤖 <b>ربات هشدار سیستم - راهنما</b>

<b>دستورات موجود:</b>

/start - شروع ربات و تأیید دسترسی ادمین
/logs - مشاهده لاگ‌های هشدارها
/status - بررسی وضعیت ربات
/logout - پایان دادن به جلسه ادمین
/help - نمایش این پیام راهنما

<b>هدف ربات:</b>
این ربات برای دریافت هشدارهای مختلف از برنامه اصلی طراحی شده است.

<b>انواع هشدارها:</b>

🚨 <b>#failed_transaction</b> - تراکنش‌های ناموفق
🔍 <b>#cryptocurrency</b> - تراکنش‌های ارز دیجیتال
⚠️ <b>#internal_alert</b> - هشدارهای داخلی سیستم

<b>نحوه استفاده:</b>

1️⃣ از /start برای احراز هویت ادمین استفاده کنید
2️⃣ شماره تلفن خود را به اشتراک بگذارید
3️⃣ پس از تأیید، هشدارهای مختلف را دریافت خواهید کرد
4️⃣ از /logs برای مشاهده جزئیات هشدارها استفاده کنید
5️⃣ از هشتگ‌ها برای جستجوی سریع استفاده کنید

<b>توجه:</b> فقط ادمین‌های تأیید شده می‌توانند از این ربات استفاده کنند.
      `.trim();

      await ctx.reply(helpText, { parse_mode: "HTML" });
    } catch (error) {
      await ctx.reply(
        "❌ خطایی در نمایش راهنما رخ داد. لطفاً بعداً دوباره تلاش کنید."
      );
    }
  },
};
